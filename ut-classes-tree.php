function print_node($node, $depth) {
	$subnodes = $node->getNodes();
	
	foreach ($subnodes as $subnode) {
		echo str_repeat("|&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;",$depth);
		
		if (sizeof($subnode->getNodes()) > 0) {
			echo "+ ";
		}
		
		echo $subnode->getPackage().'.'.$subnode->getName()."<br/>";
		
		print_node($subnode, $depth+1);
	}
}
