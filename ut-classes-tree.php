function add_subnodes($node) {
	global $ut_classes;
	
	foreach ($ut_classes as $ut_class) {
		if ($ut_class[2] == $node->getName()) {
			//echo $ut_class[0];
			$new_object = new UT_Tree($ut_class[0], $ut_class[1]);
			
